print(f"{number4:,}") # ამით ჩვენ ვეუბნებით პითონს რომ ყოველ ათასებში მძიმე (,) ჩასვას მაგ: 1,000 ან 1,000,500


number5 = float(input("enter your number: "))

print(f"{number5:,.2f}") # ამით ჩვენ პითონს ვუთხარით რომ მომხმარებლის მიერ შემოყვანილი ათწილადის წერტილის შემდეგ გამოგვიტანოს 2 ციფრი. მაგ: 12.8769908 - 12.87






